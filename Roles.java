    private void tablaRolesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaRolesMouseClicked
        // TODO add your handling code here:
int fila = tablaRoles.getSelectedRow();

if (fila != -1) { // Si se seleccionó una fila
    String id = tablaRoles.getValueAt(fila, 0).toString();
    String rol = tablaRoles.getValueAt(fila, 1).toString();
    String nombre = tablaRoles.getValueAt(fila, 2).toString();

    txtid.setText(id);
    txtRol.setText(rol);
    txtNombre.setText(nombre);

}
    }//GEN-LAST:event_tablaRolesMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
String idText = txtid.getText().trim();
String rol = txtRol.getText().trim();
String nombre = txtNombre.getText().trim();

if (idText.isEmpty() || rol.isEmpty() || nombre.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Por favor, selecciona un registro y completa todos los campos.");
    return;
}

try {
    int id = Integer.parseInt(idText);
    RolDAO rolDAO = new RolDAO();
    rolDAO.actualizarRol(id, rol, nombre);

    txtid.setText("");
    txtRol.setText("");
    txtNombre.setText("");

    JOptionPane.showMessageDialog(this, "Rol actualizado correctamente.");
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "ID inválido.");
}
    }//GEN-LAST:event_btnActualizarActionPerformed

