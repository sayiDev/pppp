    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
String idText = txtid.getText().trim();

if (idText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Por favor, selecciona un registro para eliminar.");
    return;
}

try {
    int id = Integer.parseInt(idText);
    int confirm = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar?", "Confirmar", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        RolDAO rolDAO = new RolDAO();
        rolDAO.eliminarRol(id);

        txtid.setText("");
        txtRol.setText("");
        txtNombre.setText("");

        JOptionPane.showMessageDialog(this, "Rol eliminado correctamente.");
    }
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "ID inválido.");
}
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
// PASO 1: Tomamos lo que escribió el usuario
String rol = txtRol.getText().trim();     // Cajita del rol
String nombre = txtNombre.getText().trim(); // Cajita del nombre

// PASO 2: Revisamos si dejó algo vacío
if (rol.isEmpty() || nombre.isEmpty()) {
    // Si algo está vacío, mostramos un mensaje en pantalla
    JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
    return; // Aquí se detiene, no sigue a guardar
}

// PASO 3: Si todo está lleno, guardamos los datos
RolDAO rolDAO = new RolDAO();
rolDAO.agregarRol(rol, nombre);

// PASO 4: Limpiamos las cajitas para que queden listas otra vez
txtRol.setText("");
txtNombre.setText("");

// PASO 5: Mostramos un mensaje diciendo que todo salió bien
JOptionPane.showMessageDialog(this, "Rol agregado correctamente.");
    }//GEN-LAST:event_btnAgregarActionPerformed

